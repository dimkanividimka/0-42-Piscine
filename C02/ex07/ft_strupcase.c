char	*ft_strupcase(char *str)
{
	int	i;
	int	shift;

	shift = 'A' - 'a';
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] = str[i] + shift;
		}
		i++;
	}
	return (str);
}
