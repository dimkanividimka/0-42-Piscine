char	*ft_strlowcase(char *str)
{
	int	i;
	int	shift;

	shift = 'A' - 'a';
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] = str[i] - shift;
		}
		i++;
	}
	return (str);
}
