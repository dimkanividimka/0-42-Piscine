int	ft_is_prime(int nb)
{
	int	tmp;

	if (nb < 0)
		return (0);
	if (nb == 1 || nb == 0)
		return (0);
	tmp = 2;
	while (nb / tmp >= tmp)
	{
		if (nb % tmp == 0)
			return (0);
		tmp++;
	}
	return (1);
}
